-module(t_scraping_status).

-author("vladimirb").

-export([
    to_binchar/1
]).

-export_type([
    t/0
]).

-type t() :: started | finished.

%%%===================================================================
%%% PUBLIC
%%%===================================================================

-spec to_binchar(t()) -> binary().
to_binchar(started) -> <<$s>>;
to_binchar(finished) -> <<$f>>.

